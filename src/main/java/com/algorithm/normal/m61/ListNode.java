package com.algorithm.normal.m61;

/**
 * ListNode:
 *
 * @author sunchen
 * @date 2021/2/2 10:13 下午
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
