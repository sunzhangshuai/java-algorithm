package com.algorithm.normal.m109;

/**
 * ListNode:
 *
 * @author sunchen
 * @date 2021/3/1 12:26 上午
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
