package com.algorithm.normal.h25;

/**
 * ListNode:
 *
 * @author sunchen
 * @date 2021/1/14 10:36 下午
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
